def column_correct(sudoku: list, column_no: int):
    item_list = []
    row_count = 0
    row_list = []

    for row in sudoku:
        for item in row:
            if item == row[column_no]:
                item_list.append(row[column_no])

    for item in item_list:
        if item != 0:
            if item in row_list:
                return False
            else:
                row_list.append(item)

    return True


